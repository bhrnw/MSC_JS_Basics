let totalCash = parseInt(prompt("How much money you got?"));
let watchesCount = parseInt(prompt("How many watches you wanna get?"));
let earringCount = parseInt(prompt("How many earrings you wanna get?"));
let watchesSum = parseInt(prompt("How much are the watches?"));
let earringSum = parseInt(prompt("How much are the earrings?"));
totalCash >= watchesCount*watchesSum + earringCount*earringSum